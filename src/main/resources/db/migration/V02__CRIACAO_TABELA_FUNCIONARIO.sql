-- =========================
-- TABELA FUNCIONARIO
-- =========================
CREATE TABLE funcionario (
    codigo BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(80) NOT NULL,
    nome_social VARCHAR(80),
    telefone VARCHAR(11) NOT NULL,
    email VARCHAR(80),
    data_nascimento DATE NOT NULL,
    sexo VARCHAR(9) NOT NULL,
    estado_civil VARCHAR(20) NOT NULL,
    nome_mae VARCHAR(80) NOT NULL,
    nome_pai VARCHAR(80),
    telefone_contato_emergencia VARCHAR(11),
    nome_contato_emergencia VARCHAR(80),
    data_admissao DATE NOT NULL,
    -- Endereço (Embedded)
    logradouro VARCHAR(255),
    numero VARCHAR(20),
    complemento VARCHAR(255),
    bairro VARCHAR(50),
    cep VARCHAR(20),
    codigo_cidade BIGINT,
    cpf VARCHAR(11) NOT NULL,
    rg VARCHAR(20) NOT NULL,
    pis VARCHAR(11),
    titulo_eleitor VARCHAR(13),
    carteira_trabalho VARCHAR(50),
    nacionalidade VARCHAR(100),
    naturalidade VARCHAR(100),
    cargo VARCHAR(100) NOT NULL,
    departamento VARCHAR(100) NOT NULL,
    salario NUMERIC(15,2),
    data_demissao DATE,
    tipo_contrato VARCHAR(50) NOT NULL,
    jornada_trabalho VARCHAR(100),
    situacao VARCHAR(50),
    observacoes TEXT,
    data_cadastro TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);